# Picking a random number x, and raising in to the power of another random number y
# Pick the random number by importing the random function
import random
x = random.randint(2, 23)
y = random.randint(2, 23)
print(x)
# Raise x to y as POWER
POWER = x**y
# Multiply power with pi, 3.14, as area of the circle
pi = 3.14
area = POWER * pi
print(area)
